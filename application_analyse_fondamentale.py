def calculate_performance_metrics(symbol: str, years: int) -> tuple[float, float, float]:
    """
    Calcule la performance totale, la performance annualis√©e moyenne (g√©om√©trique)
    et le R-carr√© (lin√©arit√©) d'un symbole boursier sur une p√©riode donn√©e.

    Args:
        symbol (str): Le symbole boursier (ticker).
        years (int): Le nombre d'ann√©es sur lequel calculer la performance.

    Returns:
        tuple[float, float, float]: Un tuple contenant :
                                     - La performance totale en pourcentage.
                                     - La performance annualis√©e moyenne en pourcentage.
                                     - Le R-carr√©.
                                   Retourne (NaN, NaN, NaN) si les donn√©es
                                   ne peuvent pas √™tre r√©cup√©r√©es ou si la r√©gression
                                   est impossible.
    """
    today = datetime.date.today()
    start_date = today.replace(year=today.year - years)

    data = yf.download(
        symbol,
        start=start_date.isoformat(),
        end=(today + datetime.timedelta(days=1)).isoformat(),
        progress=False,
        actions=True,
        auto_adjust=False
    )

    if data.empty or len(data) < 2:
        print(f"Attention : Donn√©es insuffisantes pour le symbole '{symbol}' sur {years} ans.")
        return float('nan'), float('nan'), float('nan')

    # Calcul de la performance totale (avec dividendes r√©investis)
    p0 = float(data["Adj Close"].iloc[0])
    p1 = float(data["Adj Close"].iloc[-1])
    total_div = float(data["Dividends"].sum())

    # La performance totale est la somme du gain en capital et des dividendes, divis√©e par le prix initial.
    # On ajoute 1 au rendement pour le calcul g√©om√©trique, puis on l'annualise.
    total_return_factor = (p1 - p0 + total_div) / p0 + 1 # Facteur de rendement total (ex: 1.77 pour 77% de gain)

    # Calcul de la performance annualis√©e moyenne (g√©om√©trique)
    # G√®re le cas o√π total_return_factor pourrait √™tre n√©gatif ou z√©ro (perte totale),
    # auquel cas l'annualisation g√©om√©trique n'est pas applicable ou donne un NaN.
    if total_return_factor <= 0:
        annualized_performance = float('nan')
    else:
        # Pour le calcul annualis√©, on divise par le nombre exact d'ann√©es couvertes par les donn√©es
        # pour une meilleure pr√©cision.
        # Calcule le nombre d'ann√©es r√©elles couvertes par les donn√©es
        actual_years_covered = (data.index[-1] - data.index[0]).days / 365.25
        if actual_years_covered <= 0: # √âvite la division par z√©ro ou racine invalide
            annualized_performance = float('nan')
        else:
            annualized_performance = (total_return_factor**(1/actual_years_covered) - 1) * 100

    rounded_annualized_performance = round(annualized_performance, 2)
    rounded_total_performance = round((total_return_factor - 1) * 100, 2) # Pour garder la performance totale aussi

    # Calcul du R-carr√© pour la lin√©arit√© du prix de cl√¥ture ajust√©
    X = np.arange(len(data)).reshape(-1, 1)
    y = data["Adj Close"].values.reshape(-1, 1)

    if np.all(y == y[0]):
        print(f"Attention : Prix de cl√¥ture constant pour le symbole '{symbol}' sur {years} ans. R-carr√© non calculable.")
        return rounded_total_performance, rounded_annualized_performance, float('nan')

    try:
        model = LinearRegression()
        model.fit(X, y)
        y_pred = model.predict(X)
        r_squared = r2_score(y, y_pred)
        rounded_r_squared = round(r_squared, 4)
    except Exception as e:
        print(f"Erreur lors du calcul du R-carr√© pour '{symbol}' sur {years} ans: {e}")
        rounded_r_squared = float('nan')

    return rounded_total_performance, rounded_annualized_performance, rounded_r_squared

